package BasicJavaPrograms;

public class ReversedString {

	public static void main(String[] args) {
		
		        String name="Vishwatech";
		        
		        int stringlength=name.length();
		        
		       
		        for(int i=stringlength-1;i>=0;i--){
					
					
					System.out.print(name.charAt(i));
		            

		         
		        }
		       
		    }		
	}

